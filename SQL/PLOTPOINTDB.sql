CREATE TABLE USERS(
    user_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(55) NOT NULL UNIQUE,
    hashed_password VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE PROFILE(
    user_id NUMBER PRIMARY KEY REFERENCES USERS(user_id),
    profile_pic VARCHAR2(255),
    Bio VARCHAR2(144)
);

CREATE TABLE FOLLOWERS(
    user_id NOT NULL REFERENCES USERS(user_id),
    follow_id NUMBER NOT NULL REFERENCES USERS(user_id),
    PRIMARY KEY(user_id, follow_id)
);

CREATE TABLE Reviews(
    review_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id NUMBER NOT NULL REFERENCES USERS(user_id),
    media_id NUMBER NOT NULL,
    media_type VARCHAR2(100) NOT NULL,
    rating NUMBER(1,0) NOT NULL,
    review_text VARCHAR(200)
);